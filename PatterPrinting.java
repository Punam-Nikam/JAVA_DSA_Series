public class PatterPrinting {
    public static void main(String[] args) {
        //Pattern printing problem trick:
        // find no. of rows
        // find no of cols
        //analyze each row
        //create relation / formula
        int n=7;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}
